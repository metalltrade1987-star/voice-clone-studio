import requests
import json

OLLAMA_URL = "http://localhost:11434/api/generate"
MODEL_NAME = "qwen2.5:14b"

SYSTEM_PROMPT = """Ты — профессиональный режиссёр озвучивания. (TTS).
Твоя задача: подготовить текст так, чтобы синтезированный голос звучал естественно и человечно.

ПРАВИЛА:
1. Расставь знаки препинания (запятые, точки, тире) для естественных пауз и интонаций.
2. Замени числа на слова: "2024" → "две тысячи двадцать четвёртый", "5 кг" → "пять килограммов".
3. Раскрой аббревиатуры: "МГУ" → "Московский государственный университет".
4. Добавь многоточие (...) для длинных смысловых пауз.
5. НЕ меняй смысл текста. НЕ добавляй от себя слова. НЕ используй SSML-теги.
6. Верни ТОЛЬКО обработанный текст, без комментариев и пояснений.
7. Если слово имеет неоднозначное ударение, поставь знак ударения (´) над гласной ударного слога: за́мок, письмА́, тО́рт.
8. НЕ используй SSML-теги, IPA или фонетическую транскрипцию.
9. Для следующих слов используй ТОЧНОЕ ударение (знак ´ после гласной):
   - Рускли´мат (не Ру́склимат, не Русклима´т), ударение в этом слове на букву "и", это важно.
   - По´чты, в этом слове ударение на букву "о".
   - [добавляй другие бренды/имена по мере необходимости]
   Если слово не в списке, расставляй ударения по правилам русского языка.
10. ВСЕГДА завершай текст точкой (.). Никогда не оставляй предложения без завершающего знака препинания.
11. НЕ добавляй слова, звуки, вздохи или междометия в конец предложений. Текст должен заканчиваться чисто.
12. СТРОГО ЗАПРЕЩЕНО добавлять любые слова, фразы, звуки или междометия, которых НЕТ в исходном тексте. Верни ТОЛЬКО обработанную версию оригинала. Никаких дополнений, комментариев или «от себя».
"""


def preprocess_text(text: str) -> str:
    """Обрабатывает текст через LLM для улучшения естественности TTS."""
    payload = {
        "model": MODEL_NAME,
        "prompt": f"{SYSTEM_PROMPT}\n\nТекст для обработки:\n{text}",
        "stream": False,
        "options": {"temperature": 0.3}  # Низкая температура для стабильности
    }

    try:
        response = requests.post(OLLAMA_URL, json=payload, timeout=180)
        response.raise_for_status()
        result = response.json().get("response", "").strip()

        if not result:
            print("⚠️ LLM вернул пустой ответ, используем исходный текст")
            return text

        return result

    except Exception as e:
        print(f"⚠️ Ошибка предобработки текста: {e}. Используем исходный текст.")
        return text