import sys
from pathlib import Path
from datetime import datetime
from src.tts_engine import TTSVoiceCloner


def main():
    # === НАСТРОЙКИ ДЛЯ ТЕСТА ===
    ref_audio = "voices/Kat-test.wav"  # ← ЗАМЕНИ НА СВОЙ ФАЙЛ
    text = ("Доброе утро! Меня зовут Екатерина, и я — менеджер по работе с проектами в компании Русклимат."
            "Мой день начинается не с кофе, а с проверки чатов и почты"
            "Пока город ещё просыпается, я уже вижу, какие задачи пришли от монтажников с объектов"
            "Что нужно согласовать с логистами и где ждёт ответа заказчик. В нашей работе важно ничего не упустить: климатическое оборудование — это не просто товар"
            "Это комфорт людей в домах и офисах. К десяти часам у меня обычно первая планёрка с командой"
            "Мы разбираем текущие проекты: кто-то завершает монтаж системы вентиляции в бизнес-центре"
            "Кому-то нужно срочно подобрать замену комплектующих, а где-то заказчик хочет изменить проект на ходу."
            "Моя задача — сделать так, чтобы все эти пазлы сложились в единую картину, без срывов сроков и нервов."
            "Обед у меня редко бывает тихим. Чаще всего это звонок с объекта или встреча с партнёром."
            "Но мне нравится этот ритм."
            "Каждый проект в нашей большой семье интересен: сегодня ты работаешь с уютной квартирой, а завтра — с промышленным складом площадью в тысячу квадратов."
            "И везде нужны разные решения, разный подход, разная энергия."
            "Во второй половине дня я обычно занимаюсь документацией и отчётами."
            "Звучит скучно, но именно здесь рождается порядок, когда все акты подписаны, сметы сведены и клиенты получили обратную связь — я чувствую, что день прожит не зря."
            "К шести вечера я обычно закрываю последние задачи, пишу себе список на завтра и выдыхаю. Знаете, самое ценное в моей работе — это не закрытые проекты."
            "Это когда через месяц звонит заказчик и говорит: „Спасибо, у нас наконец-то дышится легко“."
            "Ради таких слов стоит каждый утренний чек-лист, каждая планёрка и каждый вечерний отчёт."
            "Я горжусь тем, что делаю. Потому что за каждым проектом в Русклимате стоят не просто цифры и схемы, стоят люди, которым мы помогаем жить и работать с комфортом.")


    # Генерируем уникальное имя файла с временной меткой
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_file = f"outputs/test_output_{timestamp}.wav"
    # ===========================

    # Проверка существования эталона
    if not Path(ref_audio).exists():
        print(f"❌ ОШИБКА: Эталонный файл не найден: {ref_audio}")
        print("Положи WAV-файл (30-60 сек чистой речи) в папку voices/")
        sys.exit(1)

    # Создание папки для результатов
    Path("outputs").mkdir(exist_ok=True)

    # Инициализация и синтез
    cloner = TTSVoiceCloner()

    try:
        result_path = cloner.synthesize(
            reference_path=ref_audio,
            text=text,
            output_path=output_file
        )
        print(f"\n✅ УСПЕХ! Аудио сохранено: {result_path}")

    except Exception as e:
        print(f"\n❌ ОШИБКА при генерации: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == "__main__":
    main()